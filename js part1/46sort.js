/* **********sort method***********

 the sort() method is used to sort the elements of an array in place. By default, it sorts the
  elements as strings and orders them lexicographically (alphabetically). However, you can provide
   a custom sorting function to sort the elements in a different order based on your criteria.
*/

const fruits = ['apple', 'banana', 'cherry', 'date'];
fruits.sort();
console.log(fruits); // Output: ['apple', 'banana', 'cherry', 'date']
